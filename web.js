
function mousehover(x){
    document.getElementById(x).style.backgroundImage = "linear-gradient(rgba(0,0,0,0.7),rgba(0,0,0,0.7))";
    document.getElementById(x).style.borderRadius="24px";
    document.getElementById(x).style.display = "block";
}
function hide(x){
    document.getElementById(x).style.display = "none";
}